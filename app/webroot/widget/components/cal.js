define(function(require, exports, module) {

  module.exports.add = function(a, b) {
    return a + b;
  };

  module.exports.div = function(a, b) {
    return a / b;
  };

  module.exports.mul = function(a, b) {
    return a * b;
  };

  module.exports.min = function(a, b) {
    return a - b;
  };

});
